// 繁→简转换函数
function tradToSimp(text) {
  if (!text || typeof text !== 'string') return text;
  let result = text;
  for (const [trad, simp] of Object.entries(TRAD_TO_SIMP)) {
    result = result.split(trad).join(simp);
  }
  return result;
}

// 递归处理对象中的字符串
function convertObject(obj) {
  if (typeof obj === 'string') {
    return tradToSimp(obj);
  } else if (Array.isArray(obj)) {
    return obj.map(item => convertObject(item));
  } else if (obj !== null && typeof obj === 'object') {
    const result = {};
    for (const [key, value] of Object.entries(obj)) {
      result[key] = convertObject(value);
    }
    return result;
  }
  return obj;
}

// 处理单个文件
function processFile(filePath) {
  try {
    const content = fs.readFileSync(filePath, 'utf8');
    const data = JSON.parse(content);
    const converted = convertObject(data);
    fs.writeFileSync(filePath, JSON.stringify(converted, null, 2), 'utf8');
    console.log(`✓ 已转换: ${filePath}`);
    return true;
  } catch (err) {
    console.error(`✗ 处理失败: ${filePath} - ${err.message}`);
    return false;
  }
}

// 主程序
function main() {
  const baseDir = path.join('D:', 'Antigravity_data', 'public', 'data', 'scenarioPatterns', 'zh-CN');
  
  if (!fs.existsSync(baseDir)) {
    console.error(`目录不存在: ${baseDir}`);
    process.exit(1);
  }

  const files = fs.readdirSync(baseDir).filter(f => f.endsWith('.json'));
  console.log(`找到 ${files.length} 个 JSON 文件\n`);

  let success = 0, failed = 0;
  for (const file of files) {
    const filePath = path.join(baseDir, file);
    if (processFile(filePath)) {
      success++;
    } else {
      failed++;
    }
  }

  console.log(`\n完成: 成功 ${success}, 失败 ${failed}`);
}

main();